#ifndef ZEN_ENTERPRISE_EXCEPTIONS_HPP_INCLUDED
#define ZEN_ENTERPRISE_EXCEPTIONS_HPP_INCLUDED

#include <exception>
#include <string>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
namespace Zen {
namespace Enterprise {
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
    
class network_exception 
:   public std::exception
{
public:
    network_exception(const char* _pMessage) { m_What = _pMessage; }
    network_exception(const network_exception& _Right) { m_What = _Right.m_What; }
    virtual ~network_exception() throw() {}
    network_exception& operator=(const network_exception& _Right) { m_What = _Right.m_What; return *this; }
  virtual const char* what() const throw() { return m_What.c_str(); }
private:
    std::string m_What;
};

class disconnected_exception : public network_exception
{
public:
    disconnected_exception(const char* _pMessage) : network_exception(_pMessage) {}
    virtual ~disconnected_exception() throw() {}
};

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
}   // namespace Networking
}   // namespace Zen
//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~

#endif // ZEN_ENTERPRISE_EXCEPTIONS_HPP_INCLUDED
