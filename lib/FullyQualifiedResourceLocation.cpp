//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
FullyQualifiedResourceLocation::FullyQualifiedResourceLocation(pEndpoint_type _pEndpoint,
                                                               const std::string& _fromLocationString)
:   m_pEndpoint(_pEndpoint)
,   m_pLocation(ApplicationServerManager::getSingleton().createLocation(_fromLocationString))
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
FullyQualifiedResourceLocation::FullyQualifiedResourceLocation(pEndpoint_type _pEndpoint,
                                                               pResourceLocation_type _pLocation)
:   m_pEndpoint(_pEndpoint)
,   m_pLocation(_pLocation)
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
FullyQualifiedResourceLocation::~FullyQualifiedResourceLocation()
{
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
FullyQualifiedResourceLocation::pEndpoint_type
FullyQualifiedResourceLocation::getEndpoint() const
{
    return m_pEndpoint;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
FullyQualifiedResourceLocation::pResourceLocation_type
FullyQualifiedResourceLocation::getRelativeLocation() const
{
    return m_pLocation;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
const std::string
FullyQualifiedResourceLocation::toString() const
{
    return std::string("zen://") + m_pEndpoint->toString() + m_pLocation->toString();
}

