void
ResourceLocation::setApplicationService(pApplicationService_type _pApplicationService)
{
    m_pApplicationService = _pApplicationService;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
ResourceLocation::pApplicationService_type
ResourceLocation::getApplicationService() const
{
    return m_pApplicationService;
}

